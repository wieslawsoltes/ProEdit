namespace ProEdit.FlowDocument;

/// <summary>
/// Defines list marker styles for FlowDocument lists.
/// </summary>
public enum FlowListMarkerStyle
{
    /// <summary>
    /// No marker.
    /// </summary>
    None,

    /// <summary>
    /// Disc marker (bullet).
    /// </summary>
    Disc,

    /// <summary>
    /// Circle marker (bullet).
    /// </summary>
    Circle,

    /// <summary>
    /// Square marker (bullet).
    /// </summary>
    Square,

    /// <summary>
    /// Decimal numbering.
    /// </summary>
    Decimal,

    /// <summary>
    /// Lower Roman numerals.
    /// </summary>
    LowerRoman,

    /// <summary>
    /// Upper Roman numerals.
    /// </summary>
    UpperRoman,

    /// <summary>
    /// Lower Latin letters.
    /// </summary>
    LowerLatin,

    /// <summary>
    /// Upper Latin letters.
    /// </summary>
    UpperLatin
}
